    private void txtMovilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtMovilActionPerformed
        try {
        ChoferesBo bo= new ChoferesBo();
            Vector v1 = bo.ObtenerDatosChoferes(Integer.parseInt(txtMovil.getText()));
            if(!v1.isEmpty()){
            for(int i=0;i<v1.size();i++){
                Choferes ch = (Choferes)v1.get(i);
                txtNombreChofer.setText(ch.getLV_NOMBRES()+" "+ch.getLV_APELLIDOS());
                txtModeloVehiculo.setText(ch.getLV_MODELO());
                txtColorVehiculo.setText(ch.getLV_COLOR_VEHICULO());
                txtPlacaVehiculo.setText(ch.getLV_PLACA());
                btnRegistrar.setEnabled(true);
            }
            }else{
                JOptionPane.showMessageDialog(null, "NO EXISTE EL MOVIL: "+txtMovil.getText(), "MENSAJE ALERTA", JOptionPane.ERROR_MESSAGE);
                txtNombreChofer.setText(null);
                txtModeloVehiculo.setText(null);
                txtColorVehiculo.setText(null);
                txtPlacaVehiculo.setText(null);
                btnRegistrar.setEnabled(false);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PagosDiariosChoferes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PagosDiariosChoferes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtMovilActionPerformed

