    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        
        if(jDateFecha.getDate()==null && txtValor.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "LOS CAMPOS FECHA Y VALOR SON OBLIGATORIOS", "MENSAJE ALERTA", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                int n=0;
                int ingresoPagosDiarios=0;
                int movil=Integer.parseInt(txtMovil.getText());
                String diaf=null;
                String horasystem=null;
                        String mesf=null;
                        String dia = Integer.toString(jDateFecha.getCalendar().get(Calendar.DAY_OF_MONTH));
                        String mes = Integer.toString(jDateFecha.getCalendar().get(Calendar.MONTH)+ 1);
                        String year = Integer.toString(jDateFecha.getCalendar().get(Calendar.YEAR));
                        if(dia.length()==1){
                                    diaf="0"+dia;
                                    
                                }else{
                                    diaf=dia;
                                }
                                if (mes.length()==1){
                                    mesf="0"+mes;
                                }else{
                                    
                                    mesf=mes;
                                }
                        String fecha = (diaf + "/" + mesf+ "/" + year);
                        Date myDate = new Date();
                        SimpleDateFormat mdyFormat = new SimpleDateFormat("HH:mm:ss");
                        horasystem = mdyFormat.format(myDate);
                float valorPagar=Float.parseFloat(txtValor.getText());
                PagosDiariosBo bo = new PagosDiariosBo();
                n=bo.ValidarPagoDiarios(fecha, movil);
                if(n==0){
                    String estado=null;
                    ChoferesBo bo2= new ChoferesBo();
                    estado=bo2.EstadoMovil(movil);
                    Vector v1 = bo.ValidarPagoDiarioChofer(movil, fecha);
                    if ((int)v1.get(0) == 1 || (int)v1.get(0) == 0 || estado.equals("A")){
                        PagosDiarios in= new PagosDiarios(movil,fecha+" "+horasystem,valorPagar,lblCedulaUsuario.getText(),txtObservacion.getText());
                        ingresoPagosDiarios=bo.RegistrarPagosDiarios(in);
                        if(ingresoPagosDiarios>0){
                            JOptionPane.showMessageDialog(rootPane,"PAGO REGISTRADO CORRECTAMENTE");
                            txtMovil.setText(null);
                            txtNombreChofer.setText(null);
                            jDateFecha.setDate(null);
                            txtValor.setText(null); 
                            txtModeloVehiculo.setText(null);
                            txtColorVehiculo.setText(null);
                            txtPlacaVehiculo.setText(null);
                            txtObservacion.setText(null);
                        }
                    }else{
                        JOptionPane.showMessageDialog(null, "NO SE PUEDE REALIZAR EL PAGO DE LA FECHA "+fecha+" DEBIDO QUE EL ULTIMO PAGO TIENE REGISTRADO ES EL "+(String)v1.get(1), "MENSAJE ALERTA", JOptionPane.ERROR_MESSAGE);                  
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "EL MOVIL "+movil+" YA TIENE REGISTRADO EL PAGO EN LA FECHA "+fecha, "MENSAJE ALERTA", JOptionPane.ERROR_MESSAGE);
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(IngresoChoferes.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(IngresoChoferes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

